			</p>

			<p><strong>Ferramentas utilizadas</strong>: draw.io, Miro, Figma, Google (Documentos, Planilhas, Apresentações, Formulários, Data Studio) e Toggl Track.</p>

			<p><strong>Protótipo desenvolvido com</strong>: PHP, Bootstrap 5 e Bootstrap Icons.</p>

			<p><strong>Principais entregas do case</strong>:</p>
			<ul class="ps-5">
				<li>discovery: desk research, pesquisa quantitativa e qualitativa com usuários;</li>
				<li>benchmarking e tamanho de mercado (TAM SAM SOM);</li>
				<li>visão de produto;</li>
				<li>Lean Canvas e matriz de proposta de valor;</li>
				<li>user story mapping e roadmap;</li>
				<li>estratégia de go-to market, definição de modelo de negócio e métricas;</li>
				<li>tecnologia (API, histórias de usuários).</li>
			</ul>

			<p><strong>Principais funcionalidades do protótipo</strong>:</p>
			<ul class="ps-5">
				<li>versão web responsiva;</li>
				<li>criar coleção para residência;</li>
				<li>adicionar livros a partir do ISBN (código de barras) ou manualmente e possibilidade de editar os dados;</li>
				<li>indicar formato do livro (impresso, e-book, audiolivro);</li>
				<li>organizar livros em categorias personalizadas;</li>
				<li>adicionar anotação particular a um livro;</li>
				<li>avaliar o livro com 1 a 5 estrelas;</li>
				<li>indicar se já leu o livro (com datas relacionadas e progresso da leitura);</li>
				<li>indicar para quem o livro está emprestado;</li>
				<li>exportar dados em formato CSV;</li>
				<li>perfil da conta com informações do plano e permissão para alterar e-mail ou nome cadastrados;</li>
				<li>informação sobre os Termos de uso e Política de Privacidade;</li>
				<li>solicitação dos dados arquivados pela empresa (LGPD).</li>
			</ul>

